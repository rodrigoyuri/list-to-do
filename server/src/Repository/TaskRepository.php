    public function deleteAllById($id)
    {
        $em = $this->getEntityManager();
        $qb = $em->createQueryBuilder();

        return $qb->delete()
            ->from('App\Entity\Task', 't')
            ->where('t.id IN (:id)')
            ->setParameter('id', $id)
            ->getQuery()
            ->getResult();
